from turtle import Turtle




class Jacket(Turtle):

    def __init__(self, coordinates):
        super().__init__()
        self.shape("square")
        self.color("white")
        self.turtlesize(stretch_wid=5, stretch_len=1)
        self.pencolor("white")
        self.penup()
        self.goto(coordinates)

    def move_up(self):
        self.goto(self.xcor(), self.ycor() + 20)

    def move_down(self):
        self.goto(self.xcor(), self.ycor() - 20)

